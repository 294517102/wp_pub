package com.tjsj.wp.entity;

public class MessageSearchDataEntity {

		private String returnstatus;
		
		private String message;
		
		private String payinfo;
		
		private Integer overage;
		
		private Integer sendTotal;

		public String getReturnstatus() {
			return returnstatus;
		}

		public void setReturnstatus(String returnstatus) {
			this.returnstatus = returnstatus;
		}

		public String getMessage() {
			return message;
		}

		public void setMessage(String message) {
			this.message = message;
		}

		public String getPayinfo() {
			return payinfo;
		}

		public void setPayinfo(String payinfo) {
			this.payinfo = payinfo;
		}

		public Integer getOverage() {
			return overage;
		}

		public void setOverage(Integer overage) {
			this.overage = overage;
		}

		public Integer getSendTotal() {
			return sendTotal;
		}

		public void setSendTotal(Integer sendTotal) {
			this.sendTotal = sendTotal;
		}
		
		
		
}
